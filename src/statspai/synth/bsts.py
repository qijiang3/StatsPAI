"""
Bayesian Structural Time Series / CausalImpact.

Implements the CausalImpact methodology of Brodersen et al. (2015) using
a structural time series model with a Kalman filter/smoother.  Two public
interfaces are provided:

* ``causal_impact()`` — Google CausalImpact-style (wide DataFrame, period
  boundaries).
* ``bsts_synth()`` — SCM-style panel-data interface matching other StatsPAI
  synth modules.

Model
-----
**Local-level + regression**::

    y_t = mu_t + X_t @ beta + eps_t,   eps_t ~ N(0, sigma_obs^2)
    mu_t = mu_{t-1} + eta_t,           eta_t ~ N(0, sigma_level^2)

**Local-linear-trend** adds a stochastic slope::

    mu_t = mu_{t-1} + delta_{t-1} + eta_t
    delta_t = delta_{t-1} + zeta_t,    zeta_t ~ N(0, sigma_slope^2)

Parameters are estimated by maximum likelihood (via ``scipy.optimize``)
on the pre-intervention period.  Counterfactual predictions in the
post-period are generated by running the Kalman filter forward with the
learned parameters, then posterior simulation provides full uncertainty
quantification.

References
----------
Brodersen, K.H., Gallusser, F., Koehler, J., Remy, N. and Scott, S.L.
(2015).  "Inferring causal impact using Bayesian structural time series
models."  *Annals of Applied Statistics*, 9(1), 247-274. [@brodersen2015inferring]
"""

from __future__ import annotations

from typing import Any, Dict, List, Optional, Tuple

import numpy as np
import pandas as pd
from scipy import optimize, stats

from ..core.results import CausalResult

# ====================================================================== #
#  Kalman filter / smoother for local-level (+ optional linear-trend)
# ====================================================================== #


def _kalman_filter(
    y: np.ndarray,
    X: np.ndarray,
    beta: np.ndarray,
    sigma_obs: float,
    sigma_level: float,
    sigma_slope: float = 0.0,
    use_trend: bool = False,
) -> Dict[str, Any]:
    """Forward pass of the Kalman filter.

    State vector:
        local-level:        [mu_t]
        local-linear-trend: [mu_t, delta_t]

    Parameters
    ----------
    y : ndarray, shape (T,)
        Observed outcome series.  ``np.nan`` entries are treated as missing.
    X : ndarray, shape (T, K)
        Regressor matrix (control-unit series).
    beta : ndarray, shape (K,)
        Regression coefficients.
    sigma_obs : float
        Observation noise standard deviation.
    sigma_level : float
        Level innovation standard deviation.
    sigma_slope : float
        Slope innovation standard deviation (local-linear-trend only).
    use_trend : bool
        Whether to include the stochastic slope component.

    Returns
    -------
    dict with keys:
        filtered_state, filtered_cov, predicted_state, predicted_cov,
        log_likelihood, prediction_errors, prediction_error_vars
    """
    T = len(y)
    dim = 2 if use_trend else 1

    # Transition matrix
    F = np.eye(dim)
    if use_trend:
        F[0, 1] = 1.0  # mu_{t} = mu_{t-1} + delta_{t-1} + eta

    # State noise covariance
    Q = np.zeros((dim, dim))
    Q[0, 0] = sigma_level ** 2
    if use_trend:
        Q[1, 1] = sigma_slope ** 2

    # Observation matrix (maps state to observation, excluding regression)
    H = np.zeros((1, dim))
    H[0, 0] = 1.0

    R = np.array([[sigma_obs ** 2]])  # observation noise variance

    # Initialisation — diffuse prior
    state = np.zeros(dim)
    if T > 0 and not np.isnan(y[0]):
        state[0] = y[0] - X[0] @ beta
    cov = np.eye(dim) * 1e6

    filtered_state = np.zeros((T, dim))
    filtered_cov = np.zeros((T, dim, dim))
    predicted_state = np.zeros((T, dim))
    predicted_cov = np.zeros((T, dim, dim))
    pred_errors = np.zeros(T)
    pred_error_vars = np.zeros(T)
    log_lik = 0.0

    for t in range(T):
        # --- Predict ---
        state_pred = F @ state
        cov_pred = F @ cov @ F.T + Q

        predicted_state[t] = state_pred
        predicted_cov[t] = cov_pred

        # Observation prediction
        y_pred = (H @ state_pred)[0] + X[t] @ beta
        S = (H @ cov_pred @ H.T + R)[0, 0]  # innovation variance
        # Guard: S must be strictly positive. Can collapse to 0 when
        # sigma_obs == sigma_level == 0 (degenerate MLE on constant Y).
        S = max(S, 1e-12)

        pred_errors[t] = y[t] - y_pred if not np.isnan(y[t]) else 0.0
        pred_error_vars[t] = S

        if np.isnan(y[t]):
            # Missing observation — no update
            filtered_state[t] = state_pred
            filtered_cov[t] = cov_pred
        else:
            # --- Update (standard Kalman gain) ---
            K = (cov_pred @ H.T) / S  # shape (dim, 1)
            innovation = y[t] - y_pred
            state = state_pred + (K * innovation).ravel()
            cov = (np.eye(dim) - K @ H) @ cov_pred

            filtered_state[t] = state
            filtered_cov[t] = cov

            # Log-likelihood contribution
            log_lik += -0.5 * (np.log(2 * np.pi * S) + innovation ** 2 / S)

        # Carry forward for next iteration
        state = filtered_state[t]
        cov = filtered_cov[t]

    return {
        "filtered_state": filtered_state,
        "filtered_cov": filtered_cov,
        "predicted_state": predicted_state,
        "predicted_cov": predicted_cov,
        "log_likelihood": log_lik,
        "prediction_errors": pred_errors,
        "prediction_error_vars": pred_error_vars,
    }


def _kalman_smoother(kf: Dict[str, Any], use_trend: bool = False) -> Dict[str, Any]:
    """Rauch-Tung-Striebel backward smoother.

    Parameters
    ----------
    kf : dict
        Output of ``_kalman_filter``.
    use_trend : bool
        Must match the setting used in the filter.

    Returns
    -------
    dict with keys: smoothed_state, smoothed_cov
    """
    dim = 2 if use_trend else 1
    F = np.eye(dim)
    if use_trend:
        F[0, 1] = 1.0

    filt_s = kf["filtered_state"]
    filt_c = kf["filtered_cov"]
    pred_s = kf["predicted_state"]
    pred_c = kf["predicted_cov"]
    T = len(filt_s)

    sm_state = np.zeros_like(filt_s)
    sm_cov = np.zeros_like(filt_c)
    sm_state[-1] = filt_s[-1]
    sm_cov[-1] = filt_c[-1]

    for t in range(T - 2, -1, -1):
        # Smoother gain  G = filt_c[t] @ F.T @ P_pred^{-1}
        # via solve:  P_pred.T @ G.T = (filt_c[t] @ F.T).T
        P_pred = pred_c[t + 1] + np.eye(dim) * 1e-10
        try:
            G = np.linalg.solve(P_pred.T, (filt_c[t] @ F.T).T).T
        except np.linalg.LinAlgError:  # pragma: no cover
            # Near-singular during diffuse init — fall back to pseudoinverse
            G = filt_c[t] @ F.T @ np.linalg.pinv(P_pred)
        sm_state[t] = filt_s[t] + G @ (sm_state[t + 1] - pred_s[t + 1])
        sm_cov[t] = filt_c[t] + G @ (sm_cov[t + 1] - pred_c[t + 1]) @ G.T

    return {"smoothed_state": sm_state, "smoothed_cov": sm_cov}


# ====================================================================== #
#  Parameter estimation via MLE
# ====================================================================== #


def _estimate_beta_ridge(
    y_pre: np.ndarray,
    X_pre: np.ndarray,
    n_lambdas: int = 20,
) -> np.ndarray:
    """Ridge regression for regressor coefficients, with GCV lambda selection.

    This implements a simplified spike-and-slab prior: ridge regularisation
    with the penalty chosen by generalised cross-validation.

    Parameters
    ----------
    y_pre : ndarray, shape (T0,)
        Pre-period outcome.
    X_pre : ndarray, shape (T0, K)
        Pre-period regressor matrix.
    n_lambdas : int
        Number of lambda candidates on the log-grid.

    Returns
    -------
    beta : ndarray, shape (K,)
    """
    T0, K = X_pre.shape
    if K == 0:
        return np.array([])

    # De-mean for numerical stability
    y_mean = y_pre.mean()
    X_mean = X_pre.mean(axis=0)
    y_c = y_pre - y_mean
    X_c = X_pre - X_mean

    XtX = X_c.T @ X_c
    Xty = X_c.T @ y_c

    # Lambda grid
    lam_max = max(np.abs(Xty).max(), 1e-4)
    lambdas = np.logspace(np.log10(lam_max * 1e-4), np.log10(lam_max * 10), n_lambdas)

    best_gcv = np.inf
    best_beta = np.zeros(K)

    for lam in lambdas:
        A = XtX + lam * np.eye(K)
        try:
            beta_c = np.linalg.solve(A, Xty)
        except np.linalg.LinAlgError:  # pragma: no cover
            continue  # pragma: no cover

        # GCV score
        H = X_c @ np.linalg.solve(A, X_c.T)
        resid = y_c - X_c @ beta_c
        denom = (1.0 - np.diag(H).mean()) ** 2
        if denom < 1e-12:
            continue  # pragma: no cover
        gcv = float(np.mean(resid ** 2) / denom)

        if gcv < best_gcv:
            best_gcv = gcv
            best_beta = beta_c

    # Convert back
    intercept = y_mean - X_mean @ best_beta  # noqa: F841 — absorbed into level
    return best_beta


def _neg_log_likelihood(
    params: np.ndarray,
    y_pre: np.ndarray,
    X_pre: np.ndarray,
    beta: np.ndarray,
    use_trend: bool,
) -> float:
    """Negative log-likelihood for hyperparameter optimisation.

    Parameters
    ----------
    params : ndarray
        Log-transformed [sigma_obs, sigma_level (, sigma_slope)].
    y_pre, X_pre, beta : arrays
        Pre-period data and regression coefficients.
    use_trend : bool
        Whether the model includes a stochastic slope.
    """
    sigma_obs = np.exp(params[0])
    sigma_level = np.exp(params[1])
    sigma_slope = np.exp(params[2]) if use_trend else 0.0

    kf = _kalman_filter(
        y_pre, X_pre, beta,
        sigma_obs=sigma_obs,
        sigma_level=sigma_level,
        sigma_slope=sigma_slope,
        use_trend=use_trend,
    )
    ll = kf["log_likelihood"]
    if not np.isfinite(ll):
        return 1e12
    return -ll


def _fit_model(
    y_pre: np.ndarray,
    X_pre: np.ndarray,
    use_trend: bool = False,
) -> Dict[str, Any]:
    """Fit BSTS model on pre-period data.

    Steps:
    1. Estimate beta via ridge regression (GCV).
    2. Estimate sigma_obs, sigma_level (, sigma_slope) via MLE.
    3. Run smoother on pre-period.

    Returns
    -------
    dict with fitted parameters and smoother output.
    """
    beta = _estimate_beta_ridge(y_pre, X_pre)

    # Residual variance as initial guess
    resid = y_pre - X_pre @ beta if len(beta) > 0 else y_pre.copy()
    resid_std = max(float(np.std(resid)), 1e-6)

    # MLE for noise variances
    n_hyper = 3 if use_trend else 2
    x0 = np.log(np.full(n_hyper, resid_std * 0.5))

    result = optimize.minimize(
        _neg_log_likelihood,
        x0,
        args=(y_pre, X_pre, beta, use_trend),
        method="L-BFGS-B",
        options={"maxiter": 500, "ftol": 1e-8},
    )

    sigma_obs = np.exp(result.x[0])
    sigma_level = np.exp(result.x[1])
    sigma_slope = np.exp(result.x[2]) if use_trend else 0.0

    # Final filter + smoother on pre-period
    kf = _kalman_filter(
        y_pre, X_pre, beta,
        sigma_obs=sigma_obs,
        sigma_level=sigma_level,
        sigma_slope=sigma_slope,
        use_trend=use_trend,
    )
    sm = _kalman_smoother(kf, use_trend=use_trend)

    return {
        "beta": beta,
        "sigma_obs": sigma_obs,
        "sigma_level": sigma_level,
        "sigma_slope": sigma_slope,
        "use_trend": use_trend,
        "kf_pre": kf,
        "sm_pre": sm,
        "mle_result": result,
    }


# ====================================================================== #
#  Posterior simulation for counterfactual
# ====================================================================== #


def _simulate_counterfactual(
    model: Dict[str, Any],
    X_post: np.ndarray,
    n_simulations: int = 1000,
    rng: np.random.Generator | None = None,
) -> np.ndarray:
    """Draw from the posterior predictive distribution of the counterfactual.

    Starting from the last filtered state of the pre-period, simulate the
    state forward through the post-period using the learned transition
    dynamics and add observation noise.

    Parameters
    ----------
    model : dict
        Output of ``_fit_model``.
    X_post : ndarray, shape (T1, K)
        Post-period regressor matrix.
    n_simulations : int
        Number of posterior draws.
    rng : numpy Generator, optional

    Returns
    -------
    draws : ndarray, shape (n_simulations, T1)
        Posterior counterfactual draws.
    """
    if rng is None:
        rng = np.random.default_rng()

    T1 = X_post.shape[0]
    beta = model["beta"]
    sigma_obs = model["sigma_obs"]
    sigma_level = model["sigma_level"]
    sigma_slope = model["sigma_slope"]
    use_trend = model["use_trend"]

    dim = 2 if use_trend else 1
    F = np.eye(dim)
    if use_trend:
        F[0, 1] = 1.0

    Q = np.zeros((dim, dim))
    Q[0, 0] = sigma_level ** 2
    if use_trend:
        Q[1, 1] = sigma_slope ** 2

    # Last filtered state and covariance from pre-period
    kf_pre = model["kf_pre"]
    state_mean = kf_pre["filtered_state"][-1].copy()
    state_cov = kf_pre["filtered_cov"][-1].copy()

    draws = np.zeros((n_simulations, T1))

    for s in range(n_simulations):
        # Draw initial state from filtered posterior
        state = rng.multivariate_normal(state_mean, state_cov + np.eye(dim) * 1e-10)

        for t in range(T1):
            # State transition with noise
            state_noise = rng.multivariate_normal(np.zeros(dim), Q)
            state = F @ state + state_noise

            # Observation
            reg_component = X_post[t] @ beta if len(beta) > 0 else 0.0
            obs_noise = rng.normal(0.0, sigma_obs)
            draws[s, t] = state[0] + reg_component + obs_noise

    return draws


# ====================================================================== #
#  Public API: causal_impact
